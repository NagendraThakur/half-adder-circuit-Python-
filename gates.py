## AND Function is created
def AND (var1, var2):
    if ((var1==1) and (var2==1)):
        return 1
    else:
        return 0
## OR Function is created
def OR (var1, var2):
    if (var1==1):
        return 1
    elif (var2==1):
        return 1
    else:
        return 0
## XOR Function is created
def XOR (var1, var2):
    if (var1 != var2):
        return 1
    else:
        return 0
